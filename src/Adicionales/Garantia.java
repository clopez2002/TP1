package Adicionales;

public class Garantia {

    private

        boolean garantia;

//-------------------------------------------------------------------

    public Garantia ()
    {
        this.garantia = false;
    }

//-------------------------------------------------------------------

    public boolean tieneGarantia ()
    {
        return this.garantia;
    }

//-------------------------------------------------------------------

    public void establecerGarantia ()
    {
        this.garantia = true;
    }

//-------------------------------------------------------------------

//-------------------------------------------------------------------
}
