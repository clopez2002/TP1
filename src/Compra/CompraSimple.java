//-------------------------------------------------------------------

    public CompraSimple ()
    {

    }

//-------------------------------------------------------------------

    @Override
    public int getPrecioTotalDe()
    {
        int total = 0;
        for (Producto unProducto : compra)
            total += unProducto.getPrecio();
        return total;
    }

